package String_Programs;

public class UsingIndexOf {

	public static void main(String[] args) 
	{
		String s1= "Searching index ";
		System.out.println(s1.indexOf("e")); //1 Position
		System.out.println(s1.indexOf('g')); //8 position
		System.out.println(s1.indexOf("i", 8)); //8 Indicate from where its start the index
		
		
	}

}
